<?php

namespace YllyClicAndPay\Model;

class RiskControl
{
    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $result;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getResult()
    {
        return $this->result;
    }

    /**
     * @param string $result
     */
    public function setResult($result)
    {
        $this->result = $result;
    }
}